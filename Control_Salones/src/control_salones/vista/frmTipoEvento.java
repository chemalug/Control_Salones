    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        if (tblTipoEvento.getSelectedColumnCount() > 0) {
            frmEliminarTipoEvento delEvento = new frmEliminarTipoEvento();
            delEvento.txtCodigo.setText(tblTipoEvento.getValueAt(tblTipoEvento.getSelectedRow(), 0).toString());
            delEvento.txtNombreTipoEvento.setText(tblTipoEvento.getValueAt(tblTipoEvento.getSelectedRow(), 1).toString());
            delEvento.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Debe selecccionar una fila");
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

