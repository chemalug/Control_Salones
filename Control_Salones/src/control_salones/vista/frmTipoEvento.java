    public void consultarTipo() {
        DAO_TipoEvento cat = new DAO_TipoEvento();
        Object[] cols = {"codigo", "Nombre_Tipo_Evento"};
        ArrayList<TipoEvento> lista = cat.obtenerDatos();
        Object[][] datos = new Object[lista.size()][cols.length];
        int i = 0;
        for (TipoEvento fila : lista) {
            datos[i][0] = fila.getCodigo();
            datos[i][1] = fila.getNombre_tipo_evento();
            i++;
        }
        DefaultTableModel model = new DefaultTableModel(datos, cols);
        tblTipoEvento.setModel(model);

    }

